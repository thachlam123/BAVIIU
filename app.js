  const trail = [];
  const remote = Array.from({length: 4}, (_,i) => ({h:i*70+120, pts:[], phase:Math.random()*9}));
  const particles = Array.from({length: 120}, () => ({x:Math.random(), y:Math.random(), z:Math.random(), s:Math.random()*2+0.5, a:Math.random()}));
  function drawFX(t) {
    const c = $('#fx'); const ctx = c.getContext('2d'); const dpr = c.width/state.w;
    ctx.setTransform(dpr,0,0,dpr,0,0);
    ctx.clearRect(0,0,state.w,state.h);
    ctx.globalCompositeOperation = 'lighter';

    // particles react to scroll velocity
    particles.forEach((p, i) => {
      p.y += .0007 + Math.abs(state.velocity)*.000006*(p.z+0.4);
      p.x += Math.sin(t*.0002+p.a*6)*.00025;
      if (p.y > 1.05) { p.y = -.05; p.x = Math.random(); }
      const x = p.x*state.w + Math.sin(state.smoothScroll*.002+p.a*5)*25*p.z;
      const y = p.y*state.h;
      const r = p.s*(.8+p.z*2);
      ctx.globalAlpha = .1 + p.z*.3;
      ctx.fillStyle = `hsl(${state.hue + p.z*70}, 95%, 68%)`;
      ctx.beginPath(); ctx.arc(x,y,r,0,Math.PI*2); ctx.fill();
    });

    // remote trails
    remote.forEach((r, idx) => {
      const x = (Math.sin(t*.0002*(idx+1)+r.phase)*.38+.5)*state.w;
      const y = (Math.cos(t*.00023*(idx+1)+r.phase*1.7)*.28+.5)*state.h;
      r.pts.push({x,y,t}); if (r.pts.length > 42) r.pts.shift();
      drawTrail(ctx, r.pts, `hsla(${r.h+state.hue*.1}, 100%, 60%,`, 7);
    });
    drawTrail(ctx, trail, `hsla(${state.hue}, 100%, 62%,`, 14);

    // scroll shockwave
    if (state.scrollPulse > .01) {
      const cx = state.mouse.x*state.w, cy = state.mouse.y*state.h;
      for (let i=0;i<3;i++) {
        ctx.globalAlpha = state.scrollPulse*(.26-i*.07);
        ctx.strokeStyle = `hsl(${state.hue+i*40}, 100%, 65%)`;
        ctx.lineWidth = 1.5+i*2;
        ctx.beginPath(); ctx.arc(cx, cy, (1-state.scrollPulse)*500 + i*110, 0, Math.PI*2); ctx.stroke();
      }
      state.scrollPulse *= .92;
    }
    ctx.globalCompositeOperation = 'source-over';
  }
  function drawTrail(ctx, pts, colorPrefix, maxWidth) {
    if (pts.length < 3) return;
    for (let i=1; i<pts.length; i++) {
      const a = i/pts.length;
      ctx.globalAlpha = a*.65;
      ctx.lineWidth = maxWidth*a;
      ctx.lineCap = 'round'; ctx.lineJoin = 'round';
      ctx.strokeStyle = `${colorPrefix}${a*.55})`;
      ctx.beginPath();
      ctx.moveTo(pts[i-1].x, pts[i-1].y);
      const midx = (pts[i-1].x+pts[i].x)/2, midy = (pts[i-1].y+pts[i].y)/2;
      ctx.quadraticCurveTo(pts[i-1].x, pts[i-1].y, midx, midy);
      ctx.stroke();
    }
  }

