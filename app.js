  function observers() {
    const revealIO = new IntersectionObserver(entries => {
      entries.forEach(en => { if(en.isIntersecting) en.target.classList.add('in'); });
    }, {threshold:.12, rootMargin:'0px 0px -5% 0px'});
    $$('.reveal').forEach(el => revealIO.observe(el));

    const videoIO = new IntersectionObserver(entries => {
      entries.forEach(en => {
        const v = en.target;
        if (en.isIntersecting) v.play().catch(()=>{}); else v.pause();
      });
    }, {threshold:.25});
    $$('video.media, .gallery-item video').forEach(v => videoIO.observe(v));

    const sceneIO = new IntersectionObserver(entries => {
      entries.forEach(en => {
        if(en.isIntersecting) {
          state.activeScene = Number(en.target.dataset.scene || 0);
          state.hue = [188, 282, 145, 24, 330, 205][state.activeScene] || 188;
          $$('.nav a').forEach(a => a.classList.toggle('active', a.getAttribute('href') === `#scene-${state.activeScene+1}`));
        }
      });
    }, {threshold:.45});
    $$('.scene').forEach(s => sceneIO.observe(s));
  }

  function updateLayout(t) {
    state.scroll = scrollY;
    state.smoothScroll = lerp(state.smoothScroll, state.scroll, .11);
    state.velocity = lerp(state.velocity, state.scroll - state.lastScroll, .18);
    state.absVelocity = Math.abs(state.velocity);
    state.lastScroll = state.scroll;
    state.mouse.x = lerp(state.mouse.x, state.mouse.tx, .1);
    state.mouse.y = lerp(state.mouse.y, state.mouse.ty, .1);

    const max = document.body.scrollHeight - innerHeight;
    const prog = max > 0 ? state.scroll/max : 0;
    $('#progress').style.transform = `scaleX(${prog})`;
    root.style.setProperty('--vel', clamp(state.absVelocity/120, 0, 1).toFixed(3));
    root.style.setProperty('--hue', state.hue);

    const nav = $('.nav-wrap');
    const skew = clamp(state.velocity*.025, -13, 13);
    const sc = 1 + Math.min(.12, state.absVelocity*.0015);
    nav.style.transform = `translateX(-50%) skewX(${skew}deg) scaleX(${sc})`;

    $('[data-vel]')?.parentElement && $$('[data-vel]').forEach(x => x.textContent = (state.absVelocity/100).toFixed(2));

    $$('.scene').forEach((s, i) => {
      const r = s.getBoundingClientRect();
      const p = clamp((innerHeight - r.top) / (innerHeight + r.height), 0, 1);
      s.style.setProperty('--sceneProgress', p.toFixed(3));
      const rot = (p-.5)*16 + clamp(state.velocity*.015,-8,8);
      const depth = Math.sin(p*Math.PI);
      const stage = $('.stage', s);
      if(stage) stage.style.transform = `perspective(1100px) rotateX(${(p-.5)*10}deg) rotateY(${rot}deg) translate3d(0, ${map(p,0,1,90,-90)}px, ${depth*80}px)`;
      const strip = $('.work-strip', s);
      if(strip) strip.style.transform = `translate3d(${map(p,0,1,8,-28)}vw,0,0)`;
    });

    if (state.absVelocity > 16) document.body.classList.add('is-scrolling'); else document.body.classList.remove('is-scrolling');
    $('#cursor').style.transform = `translate3d(${state.mouse.tx*state.w}px,${state.mouse.ty*state.h}px,0) scale(${state.cursorDown ? .65 : 1 + clamp(state.absVelocity/180,0,.75)})`;
    $('#cursorLabel').textContent = $('#cursor')?.classList.contains('is-hover') ? 'BẤM' : state.absVelocity > 35 ? 'CUỘN' : state.cursorDown ? 'BẤM' : 'XEM';
  }

  function loop(t) {
    state.time = t;
    updateLayout(t);
    renderGL(t);
    drawFX(t);
    requestAnimationFrame(loop);
  }

